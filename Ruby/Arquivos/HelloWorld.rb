# O hello world em Ruby é relativamente simples. Posso imprimir na tela de duas formas inicialmente.


# Utilizando puts
# puts, imprime a informação na tela e pula uma linha
puts 'Olá, Mundo'
puts 'Pulei uma linha'


# Utilizando print
# print, imprime a informação na tela e continua na mesma linha
print 'Olá,  mundo!'
print 'Continuo na mesma linha'


# Para rodar este programa em Ruby é só ir na linha de comando e com o Ruby devidamente instalado,
# digitar ruby <nome_do_arquivo.rb>
# no caso:
#
# > ruby HelloWorld.rb