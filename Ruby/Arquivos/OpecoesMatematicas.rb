# Como realizar operações matemáticas!?
# É isto que iremos ver agora, operações matemáticas básicas.
# Utilizaremos os seguintes operadores:
#     + para adição
#     - para subtração
#     * para multiplicação
#     / para divisão
#     % para o resto  da divisão inteira

puts "Quanto é 20 + 3?"
puts 20 + 3

puts "\nQuanto é 20 - 3?"
puts 20 - 3

puts "\nQuanto é 20 * 3?"
puts 20 * 3

puts "\nQuanto é 20 / 3?"
puts 20 / 3

puts "\nQuanto é o resto da divisão inteira de 20 por 3?"
puts 20 % 3
